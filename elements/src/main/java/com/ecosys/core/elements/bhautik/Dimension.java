package com.ecosys.core.elements.bhautik;

public class Dimension {
	Measure 		length;
	Measure			breadth;
	Measure			height;
	
	public Measure getLength() {
		return length;
	}
	public void setLength(Measure length) {
		this.length = length;
	}
	public Measure getBreadth() {
		return breadth;
	}
	public void setBreadth(Measure breadth) {
		this.breadth = breadth;
	}
	public Measure getHeight() {
		return height;
	}
	public void setHeight(Measure height) {
		this.height = height;
	}
	
}
